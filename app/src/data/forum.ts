export function getThread(slug: string): ForumThread | undefined {
  return FORUM_THREADS.find((t) => t.slug === slug)
}

export function relatedThreads(thread: ForumThread, limit = 3): ForumThread[] {
  return FORUM_THREADS.filter(
    (t) =>
      t.slug !== thread.slug &&
      (t.district === thread.district || t.tags.some((tag) => thread.tags.includes(tag))),
  ).slice(0, limit)
}
